#!/usr/bin/env node

/**
 * Copy imageUrls from matched original exercises to curated exercises.
 *
 * Curated exercises (ex_curated_*) map to the same free-exercise-db entries
 * as original exercises, but were created without imageUrls. This script
 * copies the imageUrls from the originals using the exercise-mapping.json.
 *
 * Outputs SQL to stdout. Pipe to psql to apply:
 *   node scripts/copy-curated-image-urls.js | psql <connection>
 *
 * Usage: node scripts/copy-curated-image-urls.js
 */

const fs = require('fs')
const path = require('path')

const mapping = JSON.parse(
  fs.readFileSync(path.join(__dirname, 'exercise-mapping.json'), 'utf8')
)

const curated = mapping.matches.filter((m) => m.our_id.startsWith('ex_curated_'))
const nonCurated = mapping.matches.filter((m) => !m.our_id.startsWith('ex_curated_'))
const nonCuratedByTheirId = {}
for (const m of nonCurated) {
  nonCuratedByTheirId[m.their_id] = m.our_id
}

const updates = []
const noMatch = []

for (const c of curated) {
  const origId = nonCuratedByTheirId[c.their_id]
  if (origId) {
    updates.push(
      `UPDATE "ExerciseDefinition" SET "imageUrls" = ` +
      `(SELECT "imageUrls" FROM "ExerciseDefinition" WHERE id = '${origId}') ` +
      `WHERE id = '${c.our_id}' AND array_length("imageUrls", 1) IS NULL;`
    )
  } else {
    noMatch.push(c)
  }
}

console.log('-- Copy imageUrls from matched original exercises to curated exercises')
console.log('-- Generated by: node scripts/copy-curated-image-urls.js')
console.log(`-- ${updates.length} curated exercises with matching originals`)
console.log(`-- ${noMatch.length} curated exercises without matches (need separate image sync)`)
if (noMatch.length > 0) {
  console.log('--')
  console.log('-- Unmatched curated exercises:')
  for (const c of noMatch) {
    console.log(`--   ${c.our_id}: ${c.our_name} -> ${c.their_id}`)
  }
}
console.log('')
console.log('BEGIN;')
console.log('')
console.log(updates.join('\n'))
console.log('')
console.log('COMMIT;')

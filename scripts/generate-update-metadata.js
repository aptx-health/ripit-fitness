#!/usr/bin/env node

/**
 * Generate UPDATE SQL for existing exercises that already have rows in the DB.
 * Seed files use ON CONFLICT DO NOTHING, so they won't update existing rows.
 * This script produces UPDATE statements for instructions + force/mechanic/level.
 *
 * Reads:
 *   - scripts/exercise-mapping.json
 *   - scripts/validated-exercise-ids.json
 *   - free-exercise-db (fetched from GitHub)
 *
 * Writes:
 *   - scripts/update-exercise-metadata.sql
 *
 * Usage: node scripts/generate-update-metadata.js
 */

const fs = require('fs')
const path = require('path')

const EXERCISE_DB_URL =
  'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/dist/exercises.json'

async function main() {
  // Fetch free-exercise-db
  console.log('Fetching free-exercise-db...')
  const res = await fetch(EXERCISE_DB_URL)
  if (!res.ok) throw new Error(`Fetch failed: ${res.status}`)
  const freeDb = await res.json()
  const freeLookup = Object.fromEntries(freeDb.map((e) => [e.id, e]))
  console.log(`  ${freeDb.length} exercises`)

  // Load mapping + validated IDs
  const mapping = JSON.parse(
    fs.readFileSync(path.join(__dirname, 'exercise-mapping.json'), 'utf8')
  )
  const validatedIds = new Set(
    JSON.parse(
      fs.readFileSync(
        path.join(__dirname, 'validated-exercise-ids.json'),
        'utf8'
      )
    )
  )

  // Only process non-curated exercises (curated get inserted fresh via 08_curated)
  const matches = mapping.matches.filter(
    (m) => !m.our_id.startsWith('ex_curated_') && validatedIds.has(m.our_id)
  )
  console.log(`${matches.length} validated non-curated matches`)

  const escapeSQL = (str) => str.replace(/'/g, "''")

  const updates = []

  for (const m of matches) {
    const freeEx = freeLookup[m.their_id]
    if (!freeEx) continue

    const sets = []

    // Instructions
    const instrs = freeEx.instructions || []
    if (instrs.length > 0) {
      const text = instrs.map((s, i) => `${i + 1}. ${s}`).join('\n')
      sets.push(`  instructions = '${escapeSQL(text)}'`)
    }

    // Metadata from mapping
    if (m.force) sets.push(`  force = '${m.force}'`)
    if (m.mechanic) sets.push(`  mechanic = '${m.mechanic}'`)
    if (m.level) sets.push(`  level = '${m.level}'`)

    if (sets.length === 0) continue

    updates.push(
      `UPDATE "ExerciseDefinition" SET\n${sets.join(',\n')}\nWHERE id = '${m.our_id}';`
    )
  }

  const sql = `-- Update existing exercises with instructions and metadata
-- Run against staging/prod where exercises already exist
-- Seed files use ON CONFLICT DO NOTHING so they won't update existing rows
--
-- Generated by: node scripts/generate-update-metadata.js
-- Source: free-exercise-db + validated exercise mapping

BEGIN;

${updates.join('\n\n')}

COMMIT;
`

  const outPath = path.join(__dirname, 'update-exercise-metadata.sql')
  fs.writeFileSync(outPath, sql)
  console.log(`\nWrote ${updates.length} UPDATE statements to update-exercise-metadata.sql`)
  console.log(`File: ${sql.split('\n').length} lines`)
}

main().catch((err) => {
  console.error('Error:', err)
  process.exit(1)
})
